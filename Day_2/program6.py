# comparison operations

# equality operation
print(f"10 == 20 = {10 == 20}")

# not equal operation
print(f"10 != 20 = {10 != 20}")

# greater than operation
print(f"10 > 20 = {10 > 20}")

# less than operation
print(f"10 < 20 = {10 < 20}")

# greater than or equal operation
print(f"10 >= 20 = {10 >= 20}")

# less than or equal operation
print(f"10 <= 20 = {10 <= 20}")