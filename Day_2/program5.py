# mathematical operations

# addition of two numbers
# 10 + 20 + 30
# 30 + 30
# 60
print(f"10 + 20 = {10 + 20}")

# string concatenation operation
print(f"'hello' + ' ' + 'world' = {'hello' + ' ' + 'world'}")

# subtraction of two numbers
print(f"10.5 - 20 = {10.5 - 20}")

# multiplication of two numbers
print(f"10 * 20 = {10 * 20}")

# string repetition operation
# works with only string and integer data types
print(f"'hello ' * 3 = {'hello ' * 3}")
print('|-*-|-*-|' * 10)

# true division of two numbers
# always returns a float data type
print(f"true division: 10 / 3 = {10 / 3}")

# floor division of two numbers
# always returns an integer data type
print(f"floor division: 10 // 3 = {10 // 3}")

# modulus operation
# always returns a remainder
print(f"modulus: 10 % 3 = {10 % 3}")

# exponentiation operation
# used to get the power of a number
print(f"exponentiation: 2 ** 3 = {2 ** 3}")
